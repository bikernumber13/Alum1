#include "libft.h"

void		ft_bzero(void *s, size_t size)
{
	size_t	i;
	char	*str;

	i = 0;
	str = (char *)s;
	if (size == 0)
		return ;
	while (i < size)
	{
		str[i] = '\0';
		i++;
	}
}
